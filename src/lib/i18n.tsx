export type Translations = typeof PT;

const translations = { pt: PT, en: EN } as const;

interface LanguageContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}

const LanguageContext = createContext<LanguageContextValue>({
  lang: 'pt',
  setLang: () => {},
  t: PT,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>('pt');
  return (
    <LanguageContext.Provider value={{ lang, setLang, t: translations[lang] as Translations }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLang() {
  return useContext(LanguageContext);
}

type ProductMeta = { name: string; nameShort: string; description: string; productionTime: string; materials: string };

// Maps slug → camelCase key used in productMeta
const slugToMetaKey: Record<string, string> = {
  'tshirt': 'tshirt', 't-shirt': 'tshirt',
  'hoodie': 'hoodie',
  'poster': 'poster',
  'capa-telemovel': 'capatelemovel', 'capatelemovel': 'capatelemovel',
  'caneca': 'caneca',
  'autocolantes': 'autocolantes',
  'tote-bag': 'totebag', 'totebag': 'totebag',
  'quadro': 'quadro',
};

export function getProductMeta(slug: string, t: Translations): ProductMeta | null {
  const key = slugToMetaKey[slug];
  if (!key) return null;
  return (t.productMeta as Record<string, ProductMeta>)[key] ?? null;
}

export function getColorName(color: string, t: Translations): string {
  return (t.productColors as Record<string, string>)[color] ?? color;
}
